	/**
	 * 删除信息
	 * 
	 * @param mapping
	 * @param form
	 * @param request
	 * @param response
	 * @return
	 */
	public ActionForward realdelete(ActionMapping mapping, ActionForm form,
			HttpServletRequest request, HttpServletResponse response) {

		try {

			String[] ids = ParamUtils.getParameterValues(request, "id", true);
			for (int i = 0; i < ids.length; i++) {
				 
			Tool.execute("delete from user_feedback where id = '"+ids[i]+"'");
				 
			}
			return list(mapping, form, request, response);
		} catch (Exception ex) {

			log.error(ex.getMessage(), ex);
			WebDialogBox dialog = new WebDialogBox(1, "错误", ex.getMessage(),
					"返回", "javascript:window.history.back()");
			request.setAttribute("DialogBox", dialog);
			return mapping.findForward("DialogBox");
		}
	}
	
	public void export(ActionMapping mapping, ActionForm form, HttpServletRequest request, HttpServletResponse response) {
		String starttime = ParamUtils.getParameter(request, "starttime", false);
		String endtime = ParamUtils.getParameter(request, "endtime", false);
		try {
			List<Map> list = mgr.export(starttime, endtime);
			String[] titles = new String[]{"用户名称", "联系电话", "联系邮箱", "反馈内容", "反馈时间"};
			String[] column = new String[]{"username", "mobile", "email", "content", "createtime"};
			String fileName = new String("用户反馈.xls".getBytes("gb2312"), "iso8859-1");
			new ExcelUtil().ExportExcelConmon(titles, column, list, fileName, response);
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
